package br.com.fiap.infinitumspring.model;

public class CountyReceita {
    private String orgao;
    private String mes;
    private String ds_fonte_recurso;
    private String ds_cd_aplicacao_fixo;
    private String ds_alinea;
    private String ds_subalinea;
    private String vl_arrecadacao;

    public String getOrgao() {
        return orgao;
    }

    public void setOrgao(String orgao) {
        this.orgao = orgao;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getDs_fonte_recurso() {
        return ds_fonte_recurso;
    }

    public void setDs_fonte_recurso(String ds_fonte_recurso) {
        this.ds_fonte_recurso = ds_fonte_recurso;
    }

    public String getDs_cd_aplicacao_fixo() {
        return ds_cd_aplicacao_fixo;
    }

    public void setDs_cd_aplicacao_fixo(String ds_cd_aplicacao_fixo) {
        this.ds_cd_aplicacao_fixo = ds_cd_aplicacao_fixo;
    }

    public String getDs_alinea() {
        return ds_alinea;
    }

    public void setDs_alinea(String ds_alinea) {
        this.ds_alinea = ds_alinea;
    }

    public String getDs_subalinea() {
        return ds_subalinea;
    }

    public void setDs_subalinea(String ds_subalinea) {
        this.ds_subalinea = ds_subalinea;
    }

    public String getVl_arrecadacao() {
        return vl_arrecadacao;
    }

    public void setVl_arrecadacao(String vl_arrecadacao) {
        this.vl_arrecadacao = vl_arrecadacao;
    }
}
