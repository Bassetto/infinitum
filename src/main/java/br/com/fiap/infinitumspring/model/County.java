package br.com.fiap.infinitumspring.model;

public class County {
    private String municipio;
    private String municipio_extenso;

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public String getMunicipio_extenso() {
        return municipio_extenso;
    }

    public void setMunicipio_extenso(String municipio_extenso) {
        this.municipio_extenso = municipio_extenso;
    }
}
